function  sobreimg(caja) {

var sobreimg = document.getElementById (caja)

sobreimg.style.color = 'darkolivegreen'
sobreimg.style.fontSize = '15px'
}

function cambiarimg(caja){

    var cambiarimg = document.getElementById (caja)
    cambiarimg.style.color = 'darkolivegreen'
    cambiarimg.style.fontSize = '20px'

}

function salirimg(caja) {

    var salirimg = document.getElementById (caja)
    salirimg.style.color = 'black'
    salirimg.style.fontSize = '20px'
}
